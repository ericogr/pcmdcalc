class Operacoes:
	def adicao(self, a, b):
		return a + b

	def subtracao(self, a, b):
		return a - b

	def multiplicacao(self, a, b):
		return a * b

	def divisao(self, a, b):
		return a / b

	def power(self, a, b):
		return a ** b

	def square(self, a):
		return sq(a)

	def log2(self, a):
		return l2(a)